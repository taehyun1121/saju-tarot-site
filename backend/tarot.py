def card_image_url(name: str) -> str:
    if name in MAJOR_FILES:
        return f"/static/cards/메이저카드/{MAJOR_FILES[name]}"
    pip, suit = name.split("/")
    if pip in COURTS:
        fname = f"{suit} {pip}.jpg"
    elif pip == "에이스":
        fname = f"{suit} 에이스.jpg"
    else:
        fname = f"{suit}{pip}.jpg"
    return f"/static/cards/{suit}/{fname}"

