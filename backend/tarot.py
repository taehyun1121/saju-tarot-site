def get_saju_meaning(card_name: str, reversed_: bool, ilgan: str) -> str:
    trait = ILGAN_CARD_TRAITS.get(ilgan)
    if not trait:
        return ""
    ohaeng = trait["energy"]
    style  = trait["style"]
    kw     = trait["keyword"]

    # 수트 파악
    suit = None
    for s in ["완드","컵","소드","펜타클"]:
        if s in card_name:
            suit = s
            break

    if suit:
        comment = ILGAN_SUIT_COMMENT.get((ohaeng, suit), "")
        energy_desc, tone = SUIT_ENERGY.get(suit, ("변화",1))
    else:
        card_e = CARD_ENERGY.get(card_name, ("흐름",1))
        comment = ""
        energy_desc, tone = card_e[0], card_e[1]

    # 일간 성향과 카드 에너지 결합 문장
    if reversed_:
        base = f"{kw} 에너지를 가진 {ilgan} 일간에게 이 카드는 내면의 저항을 드러내요."
    else:
        base = f"{kw} 기질의 {ilgan} 일간에게 이 카드는 {energy_desc} 에너지로 공명해요."

    result = base
    if comment:
        result += f" {comment}"
    return result


def get_overall_summary(cards: list, ilgan: str = None, question: str = "") -> str:
    total = len(cards)
    reversed_count = sum(1 for c in cards if c.get("reversed"))
    upright_count = total - reversed_count

    # 수트 분포
    suit_count = {"완드":0,"컵":0,"소드":0,"펜타클":0,"메이저":0}
    for c in cards:
        name = c.get("card_name","")
        found = False
        for s in ["완드","컵","소드","펜타클"]:
            if s in name:
                suit_count[s] += 1
                found = True
                break
        if not found:
            suit_count["메이저"] += 1

    dominant = max(suit_count, key=suit_count.get)
    dom_count = suit_count[dominant]

    # 긍정/부정 판단
    pos_cards = 0
    for c in cards:
        name = c.get("card_name","")
        rev = c.get("reversed", False)
        energy = CARD_ENERGY.get(name, ("흐름",1))
        score = energy[1] if not rev else -energy[1]
        if score >= 0:
            pos_cards += 1

    overall_tone = "긍정적" if pos_cards >= total * 0.6 else ("혼재된" if pos_cards >= total * 0.4 else "도전적인")

    suit_msg = {
        "완드": "열정과 행동 에너지가 강하게 나타나고 있어요. 무언가를 시작하거나 밀고 나가야 할 시기예요.",
        "컵": "감정과 관계의 흐름이 이 상황의 중심에 있어요. 마음의 소리에 귀 기울이는 게 중요해요.",
        "소드": "사고와 결정, 그리고 갈등이 핵심이에요. 명확한 판단과 솔직한 소통이 필요한 시기예요.",
        "펜타클": "현실적이고 물질적인 부분이 중심에 있어요. 꾸준함과 실용적인 선택이 중요해요.",
        "메이저": "인생의 큰 흐름과 운명적인 에너지가 작용하고 있어요. 이 시기는 중요한 전환점이에요.",
    }

    tension_msg = ""
    if reversed_count > upright_count:
        tension_msg = f" 역방향 카드가 {reversed_count}장으로 내부 저항이나 막힘이 있는 상황이에요. 억지로 밀어붙이기보다 원인을 먼저 살피는 게 나아요."
    elif reversed_count == 0:
        tension_msg = " 모든 카드가 정방향으로, 에너지 흐름이 원활한 상태예요."

    saju_msg = ""
    if ilgan:
        trait = ILGAN_CARD_TRAITS.get(ilgan, {})
        kw = trait.get("keyword","")
        if kw:
            saju_msg = f" {ilgan} 일간({kw})의 기질로 이 상황을 바라보면, 자신의 강점을 믿고 판단하는 것이 가장 중요해요."

    q_msg = f"'{question}'에 대한 전체 흐름은 " if question else "전체 흐름은 "

    return (
        f"{q_msg}{overall_tone} 에너지로 나타나고 있어요. "
        f"{'메이저 카드' if dominant == '메이저' else dominant + ' 수트'}가 {dom_count}장으로 가장 많이 등장했어요. "
        f"{suit_msg.get(dominant, '')}"
        f"{tension_msg}"
        f"{saju_msg}"
    )


def get_meaning(card_name: str, reversed_: bool, position_name: str = "") -> str:
    base = CARD_MEANINGS.get(card_name)
    if base:
        sentence = base[1 if reversed_ else 0]
    else:
        sentence = "변화의 흐름이 이 자리에 작용하고 있어요."

    if position_name:
        prefix = POSITION_PREFIXES.get(position_name, "")
        if prefix:
            return f"{prefix}, {sentence}"
    return sentence

